export function isWall(map: GameMap, x: number, y: number): boolean {
  const mx = Math.floor(x);
  const my = Math.floor(y);
  if (mx < 0 || mx >= map.width || my < 0 || my >= map.height) return true;
  return map.walls[my][mx] !== 0;
}
